            $("form#contact_us_form").submit(function (e) {
                
                e.preventDefault();
                var form_data = $( "form#contact_us_form" ).serialize();        
                $.post('http://www.stitchmyfabric.com/secure/add_contact',
                    form_data,
                    function(data){
                        console.log(data);
                        if(data.validationerror){
                            $("#error_alert_manager p.message-area").html('<h3>'+data.validationerror+'</h3>');
                            $("#error_alert_manager").modal('show');
                        }else if(data.success){
                            document.getElementById("contact_us_form").reset();
                            $("#success_alert_manager p.message-area").html('<h3>Thank you for writing to us. Our teams will respond to your query within the next 48 hours!</h3>');
                            $("#success_alert_manager").modal('show');
                        }else if(data.error){
                            $("#error_alert_manager p.message-area").html('<h3>No data!</h3>');
                            $("#error_alert_manager").modal('show');
                        }else{
                            $("#error_alert_manager p.message-area").html('<h3>Unknown error occured!</h3>');
                            $("#error_alert_manager").modal('show');
                        }                
                    }, "json"
                ); 
            });
