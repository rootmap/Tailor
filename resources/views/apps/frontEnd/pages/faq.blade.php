                        <h3>
                           <a href="#">
                           <strong>Do you cater to international orders?</strong>
                           </a>
                        </h3>
                        <p id="faq-p">Whether you are based in Pakistan or outside, Stitch My Fabric services will be accessible to you. Our international customers, can either ship their garment to us or place an order on any Online Store and have the garment shipped directly at our headquarters. Once stitched as per your specifications, we ship back your stitched dress on your preferred location.                    </p>
