                        <h2>
                           <a id="pickup-delivery-shipment"></a>Pickup,  Delivery/Shipment
                        </h2>
                        <h3>
                           <a href="#">
                           <strong>Do you charge for picking and shipping services?</strong>
                           </a>
                        </h3>
                        <p id="faq-p">Pickup service is available for free while the delivery charges may differ depending on the location. Pickup service is not available for international customers. If you are living abroad then you can  ship your fabric directly to us or you can  place an order on any online store and have the fabric delivered to us via the online store. Once we receive your fabric, we will stitch it as per your specifications and ship it back to you on your desired address. Shipment charges may apply depending on the package size and delivery location. </p>
                        <h3>
                           <a href="#">
                           <strong>How does pick up and delivery work for international customers? </strong>
                           </a>
                        </h3>
                        <p id="faq-p">Pickup service is not available for international customers. You can  ship your fabric directly to us and simultaneously place your order on our website. You can also place an order on any online store and have the fabric delivered to us via the online store. Once we receive your fabric, we will stitch it as per your specifications and ship it back to you on your desired address. Shipment charges may apply depending on the package size and delivery location.</p>
                        <h3>
                           <a href="#">
                           <strong>How much time does it take to stitch and deliver?</strong>
                           </a>
                        </h3>
                        <p id="faq-p">Stitching time may vary from 5 to 8 days depending upon your style preference. Once your clothes are stitched, we deliver them to you on your desired location. Delivery time depends solely upon your shipment address.</p>
                        <h3>
                           <a href="#">
                           <strong>Can I have different pickup and delivery/shipment addresses each time?</strong>
                           </a>
