                           <a href="#">
                           <strong>How can I place order via an online store ?</strong>
                           </a>
                        </h3>
                        <p id="faq-p">Ordering via online store has been specially designed for women who live abroad or have busy lives. You no longer have to go to a shop to buy your fabric and then give it to your tailor for stitching. You can place your order on any online store and have the clothes directly delivered at Stitch My Fabric's Headquarters. This will save your time, fuel and energy. The steps involve are as follows: <br /><br />
                           - Placing an order on any online store and getting it delivered at Stitch My Lawn.<br>
                           - Tell us where you have placed the order. This will allow us to follow up with the online store and ensure timely delivery of your fabric.<br>
                           - Give us your measurement and styling preference.<br>
                           - We receive and stitch your fabric<br>
                           - We deliver the fabric to your desired location.<br>
                        </p>
