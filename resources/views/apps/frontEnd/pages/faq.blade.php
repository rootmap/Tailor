                        <h3>
                           <a href="#">
                           <strong>What is the difference between schedule via pick and order via online store? </strong>
                           </a>
                        </h3>
                        <p id="faq-p">In schedule via pickup, you place your order with us and give us the address from where our riders will collect the garment.  In order via online store, you place an order on any Online Store and get your fabric delivered directly to our headquarters. </p>
                        <h3>
                           <a href="#">
                           <strong>How can I place an order via pick up delivery option? </strong>
                           </a>
                        </h3>
                        <p id="faq-p">Ordering via pickup and delivery is easy, all you have to do is: <br /><br />
                           1) Signup with us and give us your measurements. You can choose from the available sizes or give us your customized measurement.<br>
                           2) You can tell us how you want your dress to be by specifying your styling preference. <br>
                           3) We pickup your fabric. You can also send your measurement garment. This will allow us to get a better understanding of your measurements &amp; style preference. <br>
                           4) We stitch your clothes in a period of 3 to 8 days. <br>
                           5) We deliver your clothes on the address provided by you earlier.<br>
                        </p>
